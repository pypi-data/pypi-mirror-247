def add(a, b):
    """
    Adds two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    return a + b


def subtract(a, b):
    """
    Subtracts two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The difference of a and b.
    """
    return a - b


def multiply(a, b):
    """
    Multiplies two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The product of a and b.
    """
    return a * b


def divide(a, b):
    """
    Divides two numbers.

    Args:
        a: The first number (dividend).
        b: The second number (divisor).

    Returns:
        The quotient of a and b.
    """
    return a / b
